package org.encap;

public class Encapsulation {
private int id ;
private String name ;
private Long phon ;

public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public Long getPhon() {
	return phon;
}
public void setPhon(Long phon) {
	this.phon = phon;
}


	
	
	
	
	
	
	
	
	
	
	
	
	
}
